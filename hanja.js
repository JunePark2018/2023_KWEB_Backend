let Nodes = [];

class Node {
  char = null;
  parent = [];
  child = [];
  x = Math.floor(Math.random() * window.innerWidth);
  y = Math.floor(Math.random() * window.innerHeight);

  constructor(char) {
    this.char = char;
    Nodes.push(this);
  }

  LinkParent (...args) {
    for (const node of args) {
      if (node instanceof Node                    // 노드가 맞고
          && node !== this                        // 현재 노드가 아니며
          && !(new Set(this.parent).has(node)))     // 이미 추가된 노드가 아닐 때
      {
        this.parent.push(node);
        node.LinkChild(this);
      }
    }
  }

  LinkChild (...args) {
    for (const node of args) {
      if (node instanceof Node                    // 노드가 맞고
          && node !== this                        // 현재 노드가 아니며
          && !(new Set(this.child).has(node)))     // 이미 추가된 노드가 아닐 때
      {
        this.child.push(node);
        node.LinkParent(this);
      }
    }
  }
}

